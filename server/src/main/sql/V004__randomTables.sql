drop table if exists DND5eRandomTableEntry;
drop table if exists DND5eRandomTable;

create table DND5eRandomTable
(
    `id`        int(11) NOT NULL AUTO_INCREMENT,
    `name`      text    NOT NULL,
    `tableType` text    NOT NULL,
    `subType`   text    NOT NULL,
    `diceRoll`  text    NOT NULL,
    PRIMARY KEY (`id`)
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8;

create table DND5eRandomTableEntry
(
    `tableId`     int(11) NOT NULL,
    `rangeLow`    int(11) NOT NULL,
    `rangeHigh`   int(11) NOT NULL,
    `name`        text    NOT NULL,
    `description` text    NOT NULL,
    PRIMARY KEY (`tableId`, `rangeLow`, `rangeHigh`),
    FOREIGN KEY (`tableId`) REFERENCES DND5eRandomTable (`id`)
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8;

